#What are negative indexes and why are they used?
'''
Convenience:
    They provide a straightforward way to access elements from the end without needing to know the length of the sequence.

Slicing from the End:
    Negative indexes are particularly useful for slicing operations.

Flexibility:
    They make code more readable and concise when working with the last few elements of a sequence.


    Example:
        To get the last element of a list:
        my_list = [10, 20, 30, 40, 50]
        print(my_list[-1])  # Output: 50
'''
