#Differentiate between append () and extend () methods?
'''
append() method use for add single element at the end of the list.
extend() method use for multiple elemet at the end of the list.

'''

fullname=["pritesh"]
fullname.append("senjaliya")
print("this is using append () method",fullname)
fullname.extend(["i","am","developer"])
print("this is using extend () method",fullname)
